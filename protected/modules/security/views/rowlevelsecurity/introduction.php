<h2>Row level security</h2>

<p><strong>Row level security</strong> restricts user access at the row (or record) level. Or, in other words, row level security means allowing a user to only access data that pertains or is relevant to him or her. For example, a college student accessing a database through a system like Banner or Owl Express to register for classes should only be able to access the data that pertains to that student.</p>

<p>A common way row level security is implemented is through the use of <strong>SQL Views</strong>. The scenario in this module illustrates how SQL Views can be used to enable row level security on a database.</p>
